def reformat_vi_sentence_accent(sentence):
    """
    Change the Vietnamese accent typing to the old format

    Parameters
    ----------
    sentence : str
        Input sentence to be reformatted
    """

    sentence = sentence.lower()
    words = sentence.split(' ')

    for index, word in enumerate(words):
        cw = re.sub(r'(^\p{P}*)([p{L}.]*\p{L}+)(\p{P}*$)',
                    r'\1/\2/\3', word).split('/')
        # print(cw)

        if len(cw) == 3:
            cw[1] = reformat_vi_word_accent(cw[1])

        words[index] = ''.join(cw)

    return ' '.join(words)


def remove_accent_typing(sentence):
    """
    Remove the accent typing of Vietnamese sentence

    Parameters
    ----------
    sentence : str
        Input sentence to be reformatted
    """

    return decode.unidecode(sentence)


